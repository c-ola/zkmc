def parse_list(text):
    coords = []
    for line in text.strip().split('\n'):
        if not line: continue
        parts = line.split(':')[1].split(',')
        coords.append((int(parts[0].strip()), int(parts[1].strip())))
    return coords

def analyze(test_coords, truth_coords, name):
    total_dist = 0
    exact_matches = 0
    max_dist = 0
    max_dist_index = 0

    for i in range(128):
        x1, z1 = test_coords[i]
        x2, z2 = truth_coords[i]

        dist = math.sqrt((x2 - x1)**2 + (z2 - z1)**2)
        total_dist += dist

        if dist == 0:
            exact_matches += 1
        if dist > max_dist:
            max_dist = dist
            max_dist_index = i

    print(f"--- {name} vs Vanilla (f64) ---")
    print(f"Exact Matches: {exact_matches} / 128")
    print(f"Average Error: {total_dist/128:.2f} chunks")
    print(f"Worst Error:   {max_dist:.2f} chunks (at Stronghold #{max_dist_index})")
    print("-" * 35 + "\n")

