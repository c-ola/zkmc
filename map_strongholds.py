def parse_list(text):
    coords = []
    for line in text.strip().split('\n'):
        if not line: continue
        parts = line.split(':')[1].split(',')
        coords.append((int(parts[0].strip()), int(parts[1].strip())))
    return coords
import matplotlib.patches as patches

points = parse_list(point_text)

fig, ax = plt.subplots()

x_vals = [p[0] for p in points]
y_vals = [p[1] for p in points]
for x, z in points:
    rect = patches.Rectangle(
            (x-7, z - 7),
            14, 14,
            linewidth=1,
            edgecolor='red',
            facecolor='red',
            alpha=0.4
            )
    ax.add_patch(rect)

ax.scatter(x_vals, y_vals, c='black', s=15, label='Stronghold Candidates', zorder=5)
ax.set_xlim(min(x_vals) - 50, max(x_vals) + 50)
ax.set_ylim(min(y_vals) - 50, max(y_vals) + 50)
ax.grid(True, linestyle=":", alpha=0.6)
ax.legend(loc='upper right')
plt.show()
